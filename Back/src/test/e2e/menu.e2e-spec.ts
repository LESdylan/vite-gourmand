/**
 * Menu API E2E Tests
 */
import { INestApplication } from '@nestjs/common';
import request from 'supertest';
import { createTestApp, testUtils } from '../test-utils';

describe('Menu API (e2e)', () => {
  let app: INestApplication;

  beforeAll(async () => {
    app = await createTestApp();
  });

  afterAll(async () => {
    await app.close();
  });

  describe('GET /api/menus', () => {
    it('should return menus (public)', async () => {
      const response = await request(app.getHttpServer())
        .get('/api/menus')
        .expect(200);

      expect(response.body.success).toBe(true);
      expect(response.body.data).toHaveProperty('items');
      expect(response.body.data).toHaveProperty('meta');
    });

    it('should support pagination', async () => {
      const response = await request(app.getHttpServer())
        .get('/api/menus?page=1&limit=5')
        .expect(200);

      expect(response.body.success).toBe(true);
      expect(Array.isArray(response.body.data.items)).toBe(true);
    });
  });

  describe('GET /api/menus/:id', () => {
    it('should return 404 for non-existent menu', async () => {
      const response = await request(app.getHttpServer())
        .get('/api/menus/99999')
        .expect(404);

      expect(response.body.success).toBe(false);
    });
  });
});
